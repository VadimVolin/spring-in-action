package fuddlang.human;

public class Human {

    private String name;
    private String speech;

    public String getName() {
        return name;
    }

    public String getSpeech() {
        return speech;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSpeech(String speech) {
        this.speech = speech;
    }

}
